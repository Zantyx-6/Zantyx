#uso del And
if True and False:
    print("And: True")
else:
    print("And: False")

#uso del Or
if True or False:
    print("Or: True")
else:
    print("Or: False")

#uso del Xor
if True ^ False:
    print("Xor: True")
else:
    print("Xor: False")

#uso del Not
if not True:
    print("Not: True")
else:
    print("Not: False") 
